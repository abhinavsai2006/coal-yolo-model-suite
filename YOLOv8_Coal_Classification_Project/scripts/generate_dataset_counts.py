#!/usr/bin/env python3
"""
Generate dataset image counts for train/val/test splits and write CSV/TXT to reports/.
"""
from pathlib import Path
import csv

root = Path('.')
reports = root / 'reports'
reports.mkdir(exist_ok=True)

def count_images(path: Path):
    exts = ['.jpg', '.jpeg', '.png', '.bmp', '.tif', '.tiff']
    cnt = 0
    for p in path.rglob('*'):
        if p.is_file() and p.suffix.lower() in exts:
            cnt += 1
    return cnt

# Candidate dataset dirs to report
data_dir = root / 'data'
subsets = {}
# Look for common splits
for candidate in ['train_balanced', 'train', 'val', 'test', 'train_balanced_split']:
    p = data_dir / candidate
    if p.exists():
        # If it's a split dir with nested train/val, count recursively
        if candidate == 'train_balanced_split' and (p / 'train').exists():
            subsets['train_balanced_split/train'] = count_images(p / 'train')
            subsets['train_balanced_split/val'] = count_images(p / 'val')
        else:
            subsets[candidate] = count_images(p)

# Also enumerate any per-class folders under data/val and data/train_balanced if not already
if (data_dir / 'val').exists() and 'val' not in subsets:
    subsets['val'] = count_images(data_dir / 'val')
if (data_dir / 'train_balanced').exists() and 'train_balanced' not in subsets:
    subsets['train_balanced'] = count_images(data_dir / 'train_balanced')

# Total images across data/* directories
total = 0
for k,v in subsets.items():
    total += v
subsets['total_images_in_reported_splits'] = total

# Also compute total images under data/ recursively
all_images = count_images(data_dir)
subsets['total_images_in_data_dir'] = all_images

# Write CSV
csv_path = reports / 'dataset_image_counts.csv'
with open(csv_path, 'w', newline='') as f:
    writer = csv.writer(f)
    writer.writerow(['subset', 'image_count'])
    for k,v in subsets.items():
        writer.writerow([k, v])

# Write text summary
txt_path = reports / 'dataset_image_counts.txt'
with open(txt_path, 'w') as f:
    f.write('Dataset image counts summary\n')
    f.write('Generated by scripts/generate_dataset_counts.py\n\n')
    for k,v in subsets.items():
        f.write(f'{k}: {v}\n')

print('Wrote', csv_path, 'and', txt_path)
